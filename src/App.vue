<template>
  <div id="app">
    <LoginUser v-if="!isLogin" @login="onLogin" />
    <TodoList :user="user" v-else />
  </div>
</template>

<script>
import LoginUser from './components/LoginUser.vue'
import TodoList from './components/TodoList'

export default {
  name: 'App',
  components: {
    LoginUser, TodoList
  },
  data() {
    return {
      isLogin: false,
      user: undefined
    }
  },
  methods: {
    onLogin(data) {
     this.isLogin = data.isSuccessLogin;
     // this.userId = data.userId;
     this.user = data.user;
    }
  }
}
</script>

